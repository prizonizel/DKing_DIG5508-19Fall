
def add_numbers(x,y):
    return x+y

def subtract_numbers(x,y):
    return x-y

def multiply_numbers(x,y):
    return x*y

def divide_numbers(x,y):
    return x/y

def exponent_numbers(x,y):
    return x**y

def difference_numbers(x,y):
    if x>=y:
        return x-y
    else:
        return y-x

import math

def pythagorean_numbers(x,y):
    return math.sqrt((x**2)+(y**2))
